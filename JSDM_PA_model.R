###starting parameters ----

#load libraries for clustering MCMC chains calculation
require(doParallel)
require(foreach)

#make a cluster for parallel MCMC chains
nchains <- 4
ncores <- nchains/2 #One core for each MCMC chains
clust <- makeCluster(ncores)
registerDoParallel(clust)

#same starting values for the four chains
nsite <- 27 #number of samples
nsp <- 33 #number of species in species matrix after sorting it
n_latent <- 2 #dimensions of random site effect alpha (W1 and W2)
np <- ncol(env) + 1

#formatting of starting parameters generated by the function 
seed_mcmc <- c(1234,4321,2345,5432) #setting seeds for reproductibility

###JSDM model presence-absence ----

#model
mod_jSDM <-
  foreach (i = 1:nchains) %dopar% {
    #infering model parameters
    T1 <- Sys.time()
    mod <- jSDM::jSDM_binomial_probit(
      #response variable
      presence_data = veg_sorted_pa, 
      #explanatory variables 
      site_formula = ~.,   
      site_data= env, 
      n_latent=2,
      site_effect="random", 
      #chains
      burnin= 40000, mcmc=40000, thin=40,
      #starting values
      alpha_start=0, beta_start=0,
      lambda_start=0, W_start=0,
      V_alpha=1, 
      #priors
      shape_Valpha=0.1,
      rate_Valpha=0.1,
      mu_beta=0, V_beta=1,
      mu_lambda=0, V_lambda=1,
      #other
      seed = seed_mcmc[i],
      verbose = 0
    )
    T2 <- Sys.time()
    mod$T_jSDM <- difftime(T2,T1)
    return(mod)
  }

stopCluster(clust) #stop cluster
save(mod_jSDM,file="UAV_mod_JSDM_4chains_PA.RData") #save the 4 chains of jSDM model

