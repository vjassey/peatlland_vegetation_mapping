###beta coefficients from the model and make a mean through iterations (param species) ----

#low density and high density parameters are used to generate confidence interval maps.

sublist <- c(1:4)

spcoefs <- future_lapply(X = sublist, future.seed = TRUE, FUN = function(x){
  k <- mod_jSDM[[x]]
  bcoefs_mean <- lapply(k$mcmc.sp, function(x) colMeans(x))
  bcoefs_mean <- do.call(rbind, bcoefs_mean)
  print(bcoefs_mean)
  colnames(bcoefs_mean) <- c("beta_(Intercept)_mean", "beta_GCC_mean", "beta_BCC_mean", "beta_NDRE_mean", "beta_BAND1_R_mean", "beta_ARI_mean", "lambda_1_mean", "lambda_2_mean")
  
  bcoefs_density_int <- lapply(k$mcmc.sp, function(x) hdi(x))
  bcoefs_lowdensity <- lapply(bcoefs_density_int, function(x) {x[1,]})
  bcoefs_highdensity <- lapply(bcoefs_density_int, function(x) {x[2,]})
  bcoefs_lowdensity <- do.call(rbind, bcoefs_lowdensity)
  bcoefs_highdensity <- do.call(rbind, bcoefs_highdensity)
  colnames(bcoefs_lowdensity) <- c("beta_(Intercept)_low", "beta_GCC_low", "beta_BCC_low", "beta_NDRE_low", "beta_BAND1_R_low", "beta_ARI_low", "lambda_1_low", "lambda_2_low")
  colnames(bcoefs_highdensity) <- c("beta_(Intercept)_high", "beta_GCC_high", "beta_BCC_high", "beta_NDRE_high", "beta_BAND1_R_high", "beta_ARI_high", "lambda_1_high", "lambda_2_high")
  
  bcoefs <- cbind(bcoefs_mean, bcoefs_lowdensity, bcoefs_highdensity)

  return(bcoefs)
})

spcoefs.mean <- apply(simplify2array(spcoefs), 1:2, mean)
spcoefs.mean <- as.data.frame(spcoefs.mean)
spcoefs.mean$CI <- rownames(spcoefs.mean) #this line if we need to sort betacoefs by CI number from network clusters

###sites coeff from the model and make a mean through iterations (param sites) ----

sublist<- c(1:4)

param_site <- future_lapply(X = sublist, future.seed = TRUE, FUN = function(x){
  k <- mod_jSDM[[x]]
  
  acoeff_mean <- apply(k$mcmc.alpha,2,mean)
  acoeff_density_int <- apply(k$mcmc.alpha,2,function(x) hdi(x))
  acoeff_lowdensity <- acoeff_density_int[1,]
  acoeff_highdensity <- acoeff_density_int[2,]
  
  Vacoeff_mean <- apply(k$mcmc.V_alpha,2,mean)
  Vacoeff_density_int <- apply(k$mcmc.V_alpha,2,function(x) hdi(x))
  Vacoeff_lowdensity <- Vacoeff_density_int[1,]
  Vacoeff_highdensity <- Vacoeff_density_int[2,]
  
  W1coeff_mean <- apply(k$mcmc.latent[["lv_1"]],2,mean)
  W1coeff_density_int <- apply(k$mcmc.latent[["lv_1"]],2,function(x) hdi(x))
  W1coeff_lowdensity <- W1coeff_density_int[1,]
  W1coeff_highdensity <- W1coeff_density_int[2,]
  
  W2coeff_mean <- apply(k$mcmc.latent[["lv_2"]],2,mean)
  W2coeff_density_int <- apply(k$mcmc.latent[["lv_2"]],2,function(x) hdi(x))
  W2coeff_lowdensity <- W2coeff_density_int[1,]
  W2coeff_highdensity <- W2coeff_density_int[2,] 
  
  return(list(alpha_mean=acoeff_mean, alpha_low=acoeff_lowdensity, alpha_high=acoeff_highdensity,
              V_alpha_mean=Vacoeff_mean, V_alpha_low=Vacoeff_lowdensity, V_alpha_high=Vacoeff_highdensity,
              W1_mean=W1coeff_mean, W1_low=W1coeff_lowdensity, W1_high=W1coeff_highdensity, 
              W2_mean=W2coeff_mean, W2_low=W2coeff_lowdensity, W2_high=W2coeff_highdensity))
})

